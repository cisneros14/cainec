                        // Re-inicializar los backgrounds de imágenes si el tema lo requiere
                        initializeBackgroundImages();
                    }

                    // Inicializar backgrounds (si el tema tiene una función para ello)
                    function initializeBackgroundImages() {
                        const elements = document.querySelectorAll('[data-bg-image]');
                        elements.forEach(el => {
                            const bgImage = el.getAttribute('data-bg-image');
                            if (bgImage) {
                                el.style.backgroundImage = `url('${bgImage}')`;

                                // Verificar si la imagen se carga correctamente
                                const img = new Image();
                                img.onerror = function () {
                                    // Si la imagen falla al cargar, usar el placeholder
                                    el.style.backgroundImage = `url('${PLACEHOLDER_IMAGE}')`;
                                };
                                img.src = bgImage;
                            }
                        });
                    }

