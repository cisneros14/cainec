                    async function loadLatestBlogEntries() {
                        try {
                            const response = await fetch('admin/api/blogs/listar.php');
                            const result = await response.json();

                            if (result.success && result.data && result.data.length > 0) {
                                // Filtrar solo las entradas de tipo "noticia" y tomar las últimas 4
                                const noticias = result.data.filter(blog => blog.tipo === 'noticia').slice(0, 4);
                                if (noticias.length > 0) {
                                    renderBlogEntries(noticias);
